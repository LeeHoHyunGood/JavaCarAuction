package com.springgame.biz.admin;

public class TimeExtensionRule {
	private int tar_idx;
	private int ter_time_extension;
	private int ter_end_time;
	public int getTar_idx() {
		return tar_idx;
	}
	public void setTar_idx(int tar_idx) {
		this.tar_idx = tar_idx;
	}
	public int getTer_time_extension() {
		return ter_time_extension;
	}
	public void setTer_time_extension(int ter_time_extension) {
		this.ter_time_extension = ter_time_extension;
	}
	public int getTer_end_time() {
		return ter_end_time;
	}
	public void setTer_end_time(int ter_end_time) {
		this.ter_end_time = ter_end_time;
	}
}
